<h2>Sewer Cleanouts  – Installation and Service</h2>
<p>Sewer cleanouts play important roles in a house’s plumbing and disposal. These ‘cleanouts’ are pipes with a cap on it. This cap will provide easy access to the sewer line, making it significantly easier to remove a blockage.</p>
<p>This lateral sewer line the cleanout is installed into is the line that connect your home to the public city sewer. Typically, this will be your responsibility to maintain.
	Cleanouts can reduce the severity of a major blockage by making it that much easier to access the pipe. This can greatly reduce the cost of repairs/restorations and avoid some uncomfortable/expensive clean-up in the building itself.
