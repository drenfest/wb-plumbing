<h3>Sewer and drain</h3>
<p>When it comes to installing new sewer lines, repairing / replacing old lines, and drain/line cleaning, there’s no-one you can rely on quite like Wally Blanton Plumbing &amp; Sewer. </p>
<h3>Hydro jetting:</h3>
<p>One of the techniques we use to clean a line, this is especially effective on grease and sludge, while still being strong enough to remove tree roots. Hydro jetting utilizes high water pressure to thoroughly clean pipes between 1 ½ inches and 14 inches, and up to 500 ft in length! This technique is viable even with some smaller access points - not always the case with power rodding.</p>
<h3>Power rodding:</h3>
<p>This uses a drain-snake like tool with a cutting edge on the end that is spun by a motor. Very effective at cutting through particularly dense blockages.</p>
<h3>Sewer televising:</h3>
<p>Camera technology allows us to easily locate known problems and identify unknown problems as well. Using a flexible rod and high-definition camera, coupled with a location transmitter, we can be sure of the state of your sewer system.</p>
<h3>Sump pump / Ejector pumps:</h3>
<p>We offer full service and installation of sump and ejector pumps. Additionally, we stock our service trucks with sump and ejector pumps so that they are ready in the event of emergency. On top of the pump options, we also offer and install battery backup systems for pump systems - saving you additional stress when the power goes out.</p>
<h3>Sewer repair and construction:</h3>
<p>We are able to install cleanout stations on the sewer line to help aid in line cleaning and maintenance, as an alternative to unnecessary digging. </p>
